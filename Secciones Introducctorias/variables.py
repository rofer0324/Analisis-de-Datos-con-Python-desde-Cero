#print(cad)
print(type(cad))

#bool
valid = False
#print(valid)
print(type(valid))

#list
lista = [1, 2, 3, "Rofer", True, [4, 5, 6], [10, 11, 12]]
print(lista, type(lista))

#tuple
tupla = (1, 2, 3, "Rofer", True, [4, 5, 6], [10, 11, 12])
print(tupla, type(tupla))

# diccionario
# llave : valor
