num1 = 5
#1. por coma
print("El valor es: ", num1)

#2. por especificador de campo
num2 = 3.3
print("El valor es: %i y %.2f" %(num1, num2))

#3. por cadena
print("El valor es: " + str(num1) + " y " + str(num2))

#4. forma directa
print(f"El valor es: {num1} o {num2}")

#Lo mismo para enteros, flotantes, cadenas.
